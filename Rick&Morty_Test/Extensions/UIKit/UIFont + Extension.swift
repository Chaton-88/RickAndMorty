    }
    
    static func gilroyMedium16() -> UIFont? {
        return UIFont(name: "gilroy-medium", size: 16)
    }
    
    static func gilroyMedium13() -> UIFont? {
        return UIFont(name: "gilroy-medium", size: 13)
    }
    
    static func gilroyMedium12() -> UIFont? {
